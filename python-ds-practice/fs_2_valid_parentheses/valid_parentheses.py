def valid_parentheses(parens):
    """Are the parentheses validly balanced?

        >>> valid_parentheses("()")
        True

        >>> valid_parentheses("()()")
        True

        >>> valid_parentheses("(()())")
        True

        >>> valid_parentheses(")()")
        False

        >>> valid_parentheses("())")
        False

        >>> valid_parentheses("((())")
        False

        >>> valid_parentheses(")()(")
        False
    """
    # lst = []
    # for p in parens:
    #     lst.append(ord(p)-39)
    # open_par = lst.count(1)
    # close_par = lst.count(2)

    # if open_par == close_par:
    #     return True
    # else: return False

    open_list = ["[","{","("]
    close_list = ["]","}",")"]

    stack = []
    for i in parens:
        if i in open_list:
            stack.append(i)
        elif i in close_list:
            pos = close_list.index(i)
            if ((len(stack) > 0) and
                (open_list[pos] == stack[len(stack)-1])):
                stack.pop()
            else:
                return False
    if len(stack) == 0:
        return True
    else:
        return False

